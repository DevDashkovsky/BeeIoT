# === INMP441 ===
# ВАЖНО ПО HARDWARE: пин L/R микрофона должен быть подтянут к GND (левый
# канал, согласован с I2S.MONO в noise.py). Если оставить L/R в воздухе,
# мик постоянно подсыпает "мусорные" сэмплы около ±2^22 по неактивному
# каналу — RMS вырастает в тысячи раз и даёт фиктивные 100–110 dB SPL даже
# в тихой комнате. JUNK_THRESHOLD ниже — софтовый workaround на этот случай,
# но он точно режет реальный громкий звук, и даташитовский offset (120)
# на отфильтрованной выборке уже неверен. Поэтому если есть возможность —
# припаять L/R к GND, поднять NOISE_JUNK_THRESHOLD до 1<<23 (фактически
# отключить фильтр) и вернуть NOISE_DB_OFFSET к 120.0.

I2S_ID             = 0
# Минимум 32 кГц — иначе BCLK падает ниже 2.048 МГц и INMP441 шлёт мусор.
I2S_SAMPLE_RATE_HZ = 32_000
I2S_BITS           = 32          # INMP441 кладёт 24 бита данных в верхние биты 32-битного слова
I2S_BUFFER_BYTES   = 4_096
NOISE_WINDOW_MS    = 200         # окно для усреднения RMS

# Выбор формата I2S и канала:
#   "MONO"          — MicroPython I2S.MONO, читает LEFT-слот.
#                     Используй когда L/R мика припаян к GND (нормальная схема).
#   "STEREO_LEFT"   — I2S.STEREO, берём только сэмплы LEFT-слота.
#                     Эквивалентно MONO по результату, нужен только для отладки.
#   "STEREO_RIGHT"  — I2S.STEREO, берём только сэмплы RIGHT-слота.
#                     Используй если L/R мика на VDD ИЛИ в воздухе с pull-up —
#                     мик в этом случае выгружает данные в RIGHT-слот, и MONO
#                     читает пустой LEFT-слот (получаем саturированный мусор
#                     ±2^23 на 95% буфера — это ровно то, что в текущих логах).
#
# Если не уверен какой ставить — поставь NOISE_DIAG_ON_BOOT=True ниже и
# посмотри вердикт diagnose(): он сам подскажет, что не так с распиновкой.
NOISE_I2S_FORMAT = "MONO"

# Включает A-weighting IIR-фильтр (IEC 61672, 3 SOS-секции) перед RMS.
# С ним flat-RMS превращается в dB(A) — то, что показывают почти все
# телефонные шумомеры, и то, что используется в стандартных дозиметрах.
# Стоит ~30% доп. CPU на цикл чтения (приемлемо при NOISE_WINDOW_MS=200).
# Если отключить — расчёт пойдёт по flat-RMS, и NOISE_DB_OFFSET ниже нужно
# опустить на ~10–15 dB обратно в сторону 85.
NOISE_A_WEIGHTING  = True

# Калибровка SPL. С A-weighting (см. выше) и warmup-фиксом базовая привязка
# по даташиту (120) даёт довольно близкие к телефонному шумомеру значения,
# но всё равно подстраивается под комнату. Эмпирически 100.0 даёт ~35 dB
# в типичной тихой комнате (как телефон с A-weighting) и ~75–85 dB на
# громких звуках. Если тихая комната у тебя показывает слишком много —
# опусти offset; если слишком мало — подними. Линейная подгонка:
#   new = old - (sensor − phone)
# Без A-weighting offset = 85.0 (под этот же ~35 dB в тишине), но потолок
# показаний при этом ограничен ~85 dB.
NOISE_DB_OFFSET    = 100.0

# Порог "мусорного" сэмпла. С warmup-фиксом и валидным L/R мусор почти
# отсутствует, поэтому поднимаем порог до 1<<22 — этого хватает измерять
# до ~95 dB SPL без срезов реального громкого звука. С A-weighting мусорные
# сэмплы зануляются (а не выбрасываются), чтобы не ломать стейт IIR-фильтра.
# Если в логах clean_count сильно меньше count — значит мусор всё-таки есть,
# тогда порог имеет смысл опустить обратно к 1<<18 и/или поправить L/R.
NOISE_JUNK_THRESHOLD = 1 << 22

# Длительность "прогрева" I2S после init. INMP441 выдаёт байты сразу, но
# внутренний 3-Гц HPF + переходный процесс ADC после cold-boot устаканиваются
# ~1–2 секунды. Без прогрева DC уплывает на миллионы единиц шкалы и забивает
# реальный сигнал даже после программного DC-removal в read(). 2000 мс
# подобрано экспериментально (после warmup DC ≈ десятки тысяч против миллионов
# в startup). Снижать ниже 500 мс не рекомендую — будут плавающие показания
# первые несколько минут после питания.
NOISE_WARMUP_MS = 2000

