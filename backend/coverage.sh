#!/bin/bash

# Запускаем тесты с подсчетом покрытия, исключая пакеты без тестов (postgres, redis) если нужно,
# но go test ./internal/... просто покажет ? для пакетов без тестов, что не влияет на % покрытия.
# Если там есть go файлы но нет тестов, это будет 0%.

echo "Запуск тестов и расчет покрытия..."

# Создаем временный файл для профиля покрытия
COVERAGE_FILE="coverage.out"

# Запускаем тесты для всех подпапок в internal
go test -coverprofile=$COVERAGE_FILE ./internal/...

# Выводим общую информацию о покрытии
echo ""
echo "Детальное покрытие по функциям:"
go tool cover -func=$COVERAGE_FILE

echo ""
echo "---------------------------------------------------"
# Извлекаем и выводим общий процент покрытия
TOTAL_COVERAGE=$(go tool cover -func=$COVERAGE_FILE | grep total | awk '{print $3}')
echo "Общий процент покрытия кода: $TOTAL_COVERAGE"
echo "---------------------------------------------------"

# Удаляем временный файл
rm $COVERAGE_FILE

